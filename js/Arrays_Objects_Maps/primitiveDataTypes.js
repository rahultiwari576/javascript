/* These five primitive types are the basic building blocks of data in JavaScript!

1. String:
A string is a sequence of characters (letters, numbers, symbols) surrounded by quotes.
You use strings to represent text in your code.
Example:
let name = "John"; // This is a string.


2. Number:
A number represents any numeric value (both integers and decimals).
You use numbers for mathematical calculations and other numeric data.

Example:
let age = 25;  // This is a number.


3. Boolean:
A boolean has only two possible values: true or false.
Booleans are used to represent yes/no or true/false conditions.
Example:
let isStudent = true;  // This is a boolean value.


4. Null:
null represents the intentional absence of any object value.
It is used to indicate that a variable is empty or has no value.
Example:
let result = null;  // This is null, meaning "no value."


5. Undefined:
undefined means a variable has been declared but not assigned a value.
If you create a variable and don't give it a value, it will be undefined.
Example:
let score;  // This variable is undefined because no value has been assigned.

*/
